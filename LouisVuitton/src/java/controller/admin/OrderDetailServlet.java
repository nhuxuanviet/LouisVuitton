    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String oid_raw = request.getParameter("id");
        
        int oid;
        OrderDetailsDAO odd = new OrderDetailsDAO();
        OrderDAO od = new OrderDAO();
        try {
            oid = Integer.parseInt(oid_raw);
            
            
            
            List<OrderDetails> listOD = odd.getOrderDetailsByOrderId(oid);
            request.setAttribute("order", od.getOrderById(oid));
            request.setAttribute("listOD", listOD);
            request.getRequestDispatcher("invoice.jsp").forward(request, response);
        } catch (Exception e) {
            System.out.println(e);
        }

    }

